// Function to render blog posts
function renderPosts(postsToRender = blogPosts) {
    const postsContainer = document.getElementById('postsContainer');
    postsContainer.innerHTML = '';

    postsToRender.forEach(post => {
        const postCard = document.createElement('div');
        postCard.className = 'post-card';
        postCard.dataset.category = post.category;
        
        const formattedDate = new Date(post.date).toLocaleDateString('en-US', {
            year: 'numeric',
            month: 'short',
            day: 'numeric'
        });

        postCard.innerHTML = `
            <div class="post-image">${post.emoji}</div>
            <div class="post-content">
                <span class="post-category">${post.category.charAt(0).toUpperCase() + post.category.slice(1)}</span>
                <h3>${post.title}</h3>
                <p class="post-excerpt">${post.excerpt}</p>
                <div class="post-meta">
                    <span>${formattedDate}</span>
                    <div class="post-tags">
                        ${post.tags.map(tag => `<span class="post-tag">${tag}</span>`).join('')}
                    </div>
                </div>
            </div>
        `;

        postCard.addEventListener('click', () => {
            showPostDetail(post);
        });

        postsContainer.appendChild(postCard);
    });
}

// Function to filter posts by category
function filterPosts(category) {
    if (category === 'all') {
        renderPosts(blogPosts);
    } else {
        const filteredPosts = blogPosts.filter(post => post.category === category);
        renderPosts(filteredPosts);
    }
}

// Filter button event listeners
document.querySelectorAll('.filter-btn').forEach(btn => {
    btn.addEventListener('click', function() {
        document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
        this.classList.add('active');
        filterPosts(this.dataset.filter);
    });
});

// Category card click event
document.querySelectorAll('.category-card').forEach(card => {
    card.addEventListener('click', function() {
        const category = this.dataset.category;
        document.querySelectorAll('.filter-btn').forEach(btn => btn.classList.remove('active'));
        const categoryBtn = document.querySelector(`[data-filter="${category}"]`);
        if (categoryBtn) {
            categoryBtn.classList.add('active');
        }
        filterPosts(category);
        document.getElementById('posts').scrollIntoView({ behavior: 'smooth' });
    });
});

// Show post detail (modal or separate page)
function showPostDetail(post) {
    // Create a modal or popup to show full content
    const modal = document.createElement('div');
    modal.style.cssText = `
        position: fixed;
        top: 0;
        left: 0;
        width: 100%;
        height: 100%;
        background-color: rgba(0,0,0,0.5);
        display: flex;
        align-items: center;
        justify-content: center;
        z-index: 1000;
    `;
    
    const content = document.createElement('div');
    content.style.cssText = `
        background: white;
        padding: 40px;
        border-radius: 8px;
        max-width: 800px;
        max-height: 80vh;
        overflow-y: auto;
        position: relative;
    `;
    
    const closeBtn = document.createElement('button');
    closeBtn.textContent = '✕';
    closeBtn.style.cssText = `
        position: absolute;
        top: 15px;
        right: 15px;
        background: none;
        border: none;
        font-size: 24px;
        cursor: pointer;
    `;
    
    content.innerHTML = `
        <h1>${post.title}</h1>
        <p style="color: #999; margin-bottom: 20px;">${post.date}</p>
        <div style="line-height: 1.8; color: #333;">
            ${post.content}
        </div>
    `;
    
    content.appendChild(closeBtn);
    modal.appendChild(content);
    
    closeBtn.addEventListener('click', () => {
        modal.remove();
    });
    
    modal.addEventListener('click', (e) => {
        if (e.target === modal) {
            modal.remove();
        }
    });
    
    document.body.appendChild(modal);
}

